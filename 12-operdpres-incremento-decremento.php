<?php
// Operadores de incremento e decremento - Aula 22
$valor = 20;

//Pré-incremento
echo ++$valor;
echo "<hr>";
echo "<br>";
////////////////////////
//Pós-incremento
$valor2 = 50;
echo $valor2++;
echo "<br>";
echo $valor2;
echo "<hr>";
echo "<br>";

////////////////////////
//Pré-decremento
$valor3 = 30;
echo --$valor3;
echo "<hr>";
echo "<br>";

//Pós-decremento
$valor4 = 40;
echo $valor4--;
echo "<br>";
echo $valor4;